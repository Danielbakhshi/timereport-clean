create policy "app_read_users"
on public.users
for select
to anon
using (true);

create policy "app_read_time_entries"
on public.time_entries
for select
to anon
using (true);

create policy "app_insert_time_entries"
on public.time_entries
for insert
to anon
with check (true);

create policy "app_delete_time_entries"
on public.time_entries
for delete
to anon
using (true);
