-- Clean Supabase setup for the time reporting app.
-- Use this in a NEW Supabase project: SQL Editor -> New query -> Run.

-- Reset only the app tables. Do not run this in a Supabase project where you have other important data in these tables.
drop table if exists public.time_entries cascade;
drop table if exists public.users cascade;

create table public.users (
  id uuid primary key,
  username text unique not null,
  password_hash text not null,
  name text not null,
  role text not null check (role in ('employee', 'admin')),
  created_at timestamptz not null default now()
);

create table public.time_entries (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.users(id) on delete cascade,
  date date not null,
  company_id text not null,
  project_id text not null,
  hours numeric not null check (hours > 0 and hours <= 24),
  note text default '',
  created_at timestamptz not null default now()
);

